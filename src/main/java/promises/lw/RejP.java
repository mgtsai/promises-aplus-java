package promises.lw;
//---------------------------------------------------------------------------------------------------------------------
/**
 * Defines {@code rejectPromise} callbacks.
 */
public interface RejP
{
    //-----------------------------------------------------------------------------------------------------------------
    /**
     * Rejects the target promise.
     *
     * @param r The reason which the target promise would be rejected with
     */
    public abstract void reject(final Throwable r);
    //-----------------------------------------------------------------------------------------------------------------
}
